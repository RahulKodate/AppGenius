  private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        // validations
        if (txtName.getText().
                trim().
                equals("") || txtHouse.getText().
                trim().
                equals("") || txtCity.getText().
                trim().
                equals("") || !isLetter(txtName.getText()) || !isLetter(txtHouse.
                getText()) || !isLetter(txtCity.getText())) {
            JOptionPane.showMessageDialog(this, "Please enter appropriate values!",
                    "Error", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        community.setCommunityName(txtName.getText());
        House house = new House();
        house.setName(txtHouse.getText());
        City city = new City();
        city.setCityName(txtCity.getText());
        community.setHouse(house);
        community.setCity(city);
        JOptionPane.showMessageDialog(this, "Successfully updated community details!",
                "Update", JOptionPane.INFORMATION_MESSAGE);
        updateCommunityDetails(false);
  }//GEN-LAST:event_btnSaveActionPerformed

