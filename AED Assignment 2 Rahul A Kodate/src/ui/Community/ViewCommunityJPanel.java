    private void populateCommunityDetails() {
        txtName.setText(community.getCommunityName());
        txtHouse.setText(community.getHouse().getName());
        txtCity.setText(community.getCity().getCityName());
    }

    private void updateCommunityDetails(Boolean editable) {
        if (editable) {
            txtName.setEnabled(true);
            txtHouse.setEnabled(true);
            txtCity.setEnabled(true);
        } else {
            txtName.setEnabled(false);
            txtHouse.setEnabled(false);
            txtCity.setEnabled(false);
        }
    }

    private boolean isDigit(String s) {
        char[] chArr = s.toCharArray();
        for (char c : chArr) {
            if (!Character.isDigit(c)) {
                return false;
            }
        }
        return true;
    }

    private boolean isLetter(String s) {
        char[] chArr = s.toCharArray();
        for (char c : chArr) {
            if (!Character.isLetter(c)) {
                return false;
            }
        }
        return true;
    }
