  private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        // validations
        Community community = new Community();
        if (txtName.getText().
                trim().
                equals("") || txtAddress.getText().
                trim().
                equals("") || txtCommunity.getText().
                trim().
                equals("") || !isLetter(txtName.getText()) || !isLetter(txtAddress.
                getText()) || !isDigit(txtCommunity.getText())) {
            JOptionPane.showMessageDialog(this, "Please enter appropriate values!",
                    "Error", JOptionPane.INFORMATION_MESSAGE);
            return;
        }
        hospital.setName(txtName.getText());
        hospital.setAddress(txtAddress.getText());
        community.setCommunityName(txtCommunity.getText());
        hospital.setCommunity(community);
        JOptionPane.showMessageDialog(this, "Successfully updated hospital details!",
                "Update", JOptionPane.INFORMATION_MESSAGE);
        updateHospitalDetails(false);
  }//GEN-LAST:event_btnSaveActionPerformed

