    private void populateHospitalDetails() {
        txtName.setText(hospital.getName());
        txtAddress.setText(hospital.getAddress());
        txtCommunity.setText(hospital.getCommunity().getCommunityName());
    }

    private void updateHospitalDetails(Boolean editable) {
        if (editable) {
            txtName.setEnabled(true);
            txtAddress.setEnabled(true);
            txtCommunity.setEnabled(true);
        } else {
            txtName.setEnabled(false);
            txtAddress.setEnabled(false);
            txtCommunity.setEnabled(false);
        }
    }

    private boolean isDigit(String s) {
        char[] chArr = s.toCharArray();
        for (char c : chArr) {
            if (!Character.isDigit(c)) {
                return false;
            }
        }
        return true;
    }

    private boolean isLetter(String s) {
        char[] chArr = s.toCharArray();
        for (char c : chArr) {
            if (!Character.isLetter(c)) {
                return false;
            }
        }
        return true;
    }
